import numpy as np

def variance(data):
    """ 
    The kernel for Variance filter. Arguments: data (2d-array).
    """
    
###--------------------------------------------------------###
###-------------------Possible errors----------------------###
###--------------------------------------------------------###    
    if data.ndim !=2:
        raise ValueError("wrong dimension!")


###--------------------------------------------------------###
###----------------------Main part-------------------------###
###--------------------------------------------------------###

     # return the sum to the convolution procedure ---> !(number)!   
    return (np.std(data))**2
    
   
###--------------------------------------------------------###
###----------------------Testing part----------------------###
###--------------------------------------------------------###

def test_variance():
    data = np.ones((100,100))
    z = variance(data)  
    assert (type(z) == np.float64)

if __name__ == "__main__":
       
    test_variance()